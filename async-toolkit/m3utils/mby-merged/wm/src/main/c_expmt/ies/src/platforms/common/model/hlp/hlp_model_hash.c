/** ApplyPearsonHash
 * \ingroup intModel
 *
 * \desc           Applies Pearson Hash to the key and returns the hash value.
 *
 * \param[in]      pindex is the Pearson hash table index.
 *
 * \param[in]      isL34 is the value of layer used in computing hash.
 *
 * \return         Hash value.
 * 
 *****************************************************************************/
static fm_uint16 ApplyPearsonHash(fm_uint pindex, fm_bool isL34)
{
    fm_uint16 pvalue;
    fm_uint16 result;

    FM_NOT_USED(isL34);

    FM_LOG_ASSERT(FM_LOG_CAT_PLATFORM,
                  pindex <= 0xFFF,
                  "%u: Invalid Pearson hash table index\n",
                  pindex);

    pvalue = hlpModelPTable[pindex] & 0xF;

    /* TODO - get an answer on whether the ptable is supposed to be used
     * differently for L34 and L234 or not
    if (isL34)
    {
        result = (pindex & 0x0FF) | (pvalue << 8);
    }
    else
    {
        result = (pindex & 0xFF0) | pvalue;
    } */
    result = (pindex & 0xFF0) | pvalue;

    return result;

}   /* end ApplyPearsonHash */


