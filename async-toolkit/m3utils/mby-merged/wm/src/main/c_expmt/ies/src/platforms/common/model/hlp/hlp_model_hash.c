/*****************************************************************************/
/** CreateL234HashKey
 * \ingroup intModel
 *
 * \desc            Combines bits to form L234 key, according to L234_HASH_CFG
 *                  register; does symmetrization as necessary.
 *
 * \param[in]       model points to the switch data structure.
 *
 * \param[in]       isIP is the value indicating whether the frame is IPv4/IPv6.
 *
 *****************************************************************************/
static void CreateL234HashKey(hlp_model *model, fm_bool isIP)
{
    hlp_modelState         *state = &model->packetState;
    hlpFwdHashingCfg       fwdHashingCfg;
    fm_macaddr             a;
    fm_macaddr             b;
    fm_macaddr             dmac;
    fm_macaddr             dmac2;
    fm_macaddr             smac;
    fm_macaddr             smac2;
    fm_macaddr             xor;
    fm_uint16              vlan;
    fm_int                 i;
    fm_int                 s;

    GetFwdHashingCfg(model, &fwdHashingCfg);
    dmac = ( fwdHashingCfg.USE_DMAC ?
             state->L2_DMAC:
             FM_LITERAL_U64(0) );
    smac = ( fwdHashingCfg.USE_SMAC ?
             state->L2_SMAC :
             FM_LITERAL_U64(0) );
    xor = (state->L2_DMAC ^ state->L2_SMAC) & FM_LITERAL_U64(0xFFFFFFFFFFFF);

    if ( fwdHashingCfg.SYMMETRIC )
    {
        FM_CLEAR(dmac2);
        FM_CLEAR(smac2);

        for (i = 0, s = 0; i < 6; i++, s += 8)
        {
            a = (fm_byte) ( (dmac >> s) & 0xFF );
            b = (fm_byte) ( (smac >> s) & 0xFF );

            dmac2 |= MAX(a, b) << s;
            smac2 |= MIN(a, b) << s;
        }
    }
    else
    {
        dmac2 = dmac;
        smac2 = smac;
    }
    
    for (i = 0, s = 40; i < 6; i++, s -= 8)
    {
        state->HASH_KEYS.l234Key[i] = (fm_byte) ( (dmac2 >> s) & 0xFF );
        state->HASH_KEYS.l234Key[6 + i] = (fm_byte) ( (smac2 >> s) & 0xFF );
    }

    if ( fwdHashingCfg.USE_TYPE && ( state->L2_ETYPE >= 0x0600 ) )
    {
        for (i = 0, s = 8; i < 2; i++, s -= 8)
        {
            state->HASH_KEYS.l234Key[12 + i] =
                (fm_byte) ((state->L2_ETYPE >> s) & 0xFF);
        }
    }

    vlan = 0;

    if (fwdHashingCfg.USE_VID)
    {
        vlan = state->L2_IVID1;
    }

    if (fwdHashingCfg.USE_VPRI)
    {
        vlan |= state->QOS_L2_VPRI1 << 12;
    }

    for (i = 0, s = 8; i < 2; i++, s -= 8)
    {
        state->HASH_KEYS.l234Key[14 + i] = (fm_byte) ((vlan >> s) & 0xFF);
    }
    
    if (fwdHashingCfg.USE_METADATA)
    {
        //check for TYPE = 0x16 or 0x17 (DSI TX) && MD[22][2] == 1
        if (((state->PKT_META[0] == 0x16) || (state->PKT_META[0] == 0x17)) &&
            (state->PKT_META[22] & 0x4))
        {
            //LAG Hash Mod from MetaData
            state->HASH_KEYS.l234Key[16] = state->PKT_META[23];
        }      
    }
    /* Extract the configuration information and forward it to the HASH stage */
    state->HASH_KEYS.arpEcmpCfg = fwdHashingCfg.ECMP_ROTATION;
    state->HASH_KEYS.zeroL2 = ( isIP && !fwdHashingCfg.USE_L2_IF_IP );
    state->HASH_KEYS.zeroL34 = !isIP;
    state->HASH_KEYS.useL34 = fwdHashingCfg.USE_L34;
    state->HASH_KEYS.rotA = fwdHashingCfg.ROTATION_A;
    state->HASH_KEYS.rotB = fwdHashingCfg.ROTATION_B;

}   /* end CreateL234HashKey */


/*****************************************************************************/
/** ComputeL234Hash
 * \ingroup intModel
 *
 * \desc            Computes L234 Hash value
 *
 * \param[in]       model points to the switch data structure.
 *
 *****************************************************************************/
static void ComputeL234Hash(hlp_model     *model)
{
    hlp_modelState     *state = &model->packetState;
    fm_uint64          hash;

    hash = state->HASH_KEYS.zeroL2 ? FM_LITERAL_U64(0) : state->HASH_KEYS.crc234;

    if ( state->HASH_KEYS.useL34 && !state->HASH_KEYS.zeroL34 )
    {
        hash ^= ((state->HASH_KEYS.crc34 & FM_LITERAL_U64(0xFFFFFF)) << 24) | 
                (state->HASH_KEYS.crc34 & FM_LITERAL_U64(0xFFFFFF));
    }

    state->HASH_ROT_A_PTABLE_INDEX = hash >> (MIN(state->HASH_KEYS.rotA, 3) * 12);
    state->HASH_ROT_A_PTABLE_INDEX &= FM_LITERAL_U64(0xFFF);
    state->HASH_ROT_B_PTABLE_INDEX = hash >> (MIN(state->HASH_KEYS.rotB, 3) * 12);
    state->HASH_ROT_B_PTABLE_INDEX &= FM_LITERAL_U64(0xFFF);
    
    state->HASH_ROT_A = ApplyPearsonHash(state->HASH_ROT_A_PTABLE_INDEX, FALSE);
    state->HASH_ROT_B = ApplyPearsonHash(state->HASH_ROT_B_PTABLE_INDEX, FALSE);

}   /* end ComputeL234Hash */

/*****************************************************************************/
/** ComputeL34Hash
 * \ingroup intModel
 *
 * \desc            Computes L34 Hash value  
 *
 * \param[in]       model points to the switch data structure.
 *
 *****************************************************************************/
static void ComputeL34Hash(hlp_model *model)
{
    hlp_modelState *state = &model->packetState;
    const fm_uint64    mask = FM_LITERAL_U64(0xFFF);
    fm_uint32          mult;
    fm_uint32          operand;
    fm_uint16          ptableOut;
    fm_int             i;
    fm_uint            pindex;

    switch (state->HASH_KEYS.arpEcmpCfg)
    {
        case 0:  pindex = (fm_uint) (state->HASH_KEYS.crc34 & mask); break;
        case 1:  pindex = (fm_uint) ((state->HASH_KEYS.crc34 >> 12) & mask); break;
        default: pindex = (fm_uint) (state->HASH_KEYS.crc34 & mask);         break;
    }

    ptableOut = ApplyPearsonHash(pindex, TRUE);

    state->RAW_HASH =  ( ( ptableOut & 0xF ) << 8 ) | ( ( pindex >> 4 ) & 0xFF );
    
    operand = ( ( ptableOut & 0xF ) << 8 ) | (( pindex >> 4) & 0xFF );

    for (i = 0, mult = 16; i < 16; i++, mult = (mult + 1) % 16)
    {
        state->ARP_HASH[i] = (mult * operand) >> 12;
    }

}   /* end ComputeL34Hash */


/*****************************************************************************/
/** CrcKey
 * \ingroup intModel
 *
 * \desc            Performs two 32-bit CRC functions.
 *
 * \param[in,out]   keys is the variable the CRC functions are performs on.  
 *
 * \param[in]       length is the length of the keys on which the funtion is
 *                  operated.
 *
 * \param[in]       width is the width of the keys on which the funtion is
 *                  operated.
 *
 * \return          CRC function result.
 *
 *****************************************************************************/
static fm_uint64 CrcKey(fm_byte *keys, fm_int length, fm_int width)
{
    fm_uint64 cHash;
    fm_uint64 eHash;
    fm_uint64 mask;

    eHash = ((fm_uint64) fmCrc32ByteSwap(keys, length)) & FM_LITERAL_U64(0xFFFFFFFF);
    cHash = ((fm_uint64) fmCrc32CByteSwap(keys, length)) & FM_LITERAL_U64(0xFFFFFFFF);

    mask = (FM_LITERAL_U64(1) << width) - FM_LITERAL_U64(1);

    return ( ((cHash << 32) | eHash) & mask );

}   /* end CrcKey */


