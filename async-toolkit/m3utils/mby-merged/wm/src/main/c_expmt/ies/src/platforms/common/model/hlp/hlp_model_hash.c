/*****************************************************************************/
/** hlpModelHash
 * \ingroup intModel
 *
 * \desc            Calculate hash values for the purpose of ECMP and LAG.
 *
 * \param[in]       model points to the switch data structure.
 *
 *****************************************************************************/
void hlpModelHash(hlp_model *model)
{
    fm_bool            isIP;
    hlp_modelState     *state = &model->packetState;

   //if(testPlusArgs("HLP_HASH_WM_PRINT_VERBOSE") >= 0)
   //    hashDisplayVerbose = testPlusArgs("HLP_HASH_WM_PRINT_VERBOSE");
   //WM_DISPLAY(hashDisplayVerbose, FM_CAT_MODEL_HASH, "hashDisplayVerbose=%0d\n", hashDisplayVerbose)

    isIP = ( state->IS_IPV4 || state->IS_IPV6 );
    /* generate the HASH_KEYS */
    CreateL234HashKey(model, isIP);

    state->HASH_KEYS.crc234 = CrcKey(state->HASH_KEYS.l234Key,
                                     sizeof(state->HASH_KEYS.l234Key),
                                     48);
    
    state->HASH_KEYS.crc34 = state->L34_HASH;
    
    ComputeL34Hash(model);

    ComputeL234Hash(model);

    if (!state->DISABLE_DBG_DUMP)
        HLP_MODEL_DBG_DUMP(model, hlpModelDbgDumpHash);

}   /* end hlpModelHash */

