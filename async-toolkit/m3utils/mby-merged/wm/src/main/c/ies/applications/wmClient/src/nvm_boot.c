void loadCfg(int cpkLogPort, int quad_mode[8])
{
	int defaultVid = 1;
	int portMask = 0xFFFFFF;
	fm_uint64 val;
	int port;
	int i;

	/* Load Parser uCode */
	loaduCode();

	if (outFp) fprintf(outFp, "# End of PARSER section\n");

	/* Minic HIG version */
	/* TODO: update scratch registers
	 * write64(HLP_BSM_SCRATCH(4094,0 ),  0x107370005);
	 * write64(HLP_BSM_SCRATCH(4093, 0),  0x0);
	 */

	/* MAP_CPM_FN and MOD_CPM_FN should bet set to 0 (default).
	   The API will set it appropriately when IPsec is enabled */

	/* NOTE: all traffic will be classified as vlanId of 1 so it can be
	 * forwarded. However tagged traffic will be forwarded with the original
	 * tag and not vlan id of 1 */

	/* We just need to force OTR_VLAN1 = VID 1 and then
	   the mapper will set ACT4[6:4] accordingly. This is accomplished by
	   using force key 82. */

	if (outFp) fprintf(outFp, "# MAP_DOMAIN\n");
	/* Enable learning, force l2_domain to 0 */
	/* Refer to
	 * https://securewiki.ith.intel.com/display/SRDSoftware/Architecture
	 * +-+API+5.x+-+Domain+Management#Architecture-API5.x-DomainManagement
	 * -apiinitIESAPIinitialization
	 * */
	i = 0;
	write64(HLP_MAP_DOMAIN_TCAM(i, 0),  0);
	/* !!!!!!!!!!!!!! WARNING!!!!!!!!!!!!!
	 * word index here but in API5 it will be qword index.
	 * So it will be 1 in API5 code instead of 2 here */
	write64(HLP_MAP_DOMAIN_TCAM(i, 2),  0);
	val = 0;
	FM_SET_BIT(val, HLP_MAP_DOMAIN_ACTION0, LEARN_EN, 1);
	FM_SET_FIELD(val, HLP_MAP_DOMAIN_ACTION0, PRI_SOURCE, 0xFF);
	write64(HLP_MAP_DOMAIN_ACTION0(i, 0),  val);

	if (outFp) fprintf(outFp, "# MAP_DGLORT\n");
	/* First entry in the HLP-SAS - Glocal Resource Tags section.
	 * For directed send to a specific port */
	val = 0;
	FM_SET_FIELD(val, HLP_MAP_DGLORT_TCAM, KEY, 0x000001 & 0x0000FF);
	FM_SET_FIELD(val, HLP_MAP_DGLORT_TCAM, KEY_INVERT,  ~0x000001 & 0x0000FF);
	write64(HLP_MAP_DGLORT_TCAM(0, 0),  val);
	val = 0;
	FM_SET_FIELD(val, HLP_MAP_DGLORT_ACTION, BASE, PORT_GLORT_BASE);
	FM_SET_FIELD(val, HLP_MAP_DGLORT_ACTION, LENGTH, 5);
	FM_SET_FIELD(val, HLP_MAP_DGLORT_ACTION, START, 0);
	FM_SET_FIELD(val, HLP_MAP_DGLORT_ACTION, ROT, 16);
	FM_SET_BIT(val, HLP_MAP_DGLORT_ACTION, ENABLE, 1);
	write64(HLP_MAP_DGLORT_ACTION(0, 0), val);

	/* Second entry in the HLP-SAS - Glocal Resource Tags section.
	 * For flooding or L2 switching */
	val = 0;
	FM_SET_FIELD(val, HLP_MAP_DGLORT_TCAM, KEY, 0x140001 & 0xFF00FF);
	FM_SET_FIELD(val, HLP_MAP_DGLORT_TCAM, KEY_INVERT, ~0x140001 & 0xFF00FF);
	write64(HLP_MAP_DGLORT_TCAM(1, 0),  val);
	val = 0;
	FM_SET_FIELD(val, HLP_MAP_DGLORT_ACTION, BASE, 0);
	FM_SET_FIELD(val, HLP_MAP_DGLORT_ACTION, LENGTH, 0);
	FM_SET_FIELD(val, HLP_MAP_DGLORT_ACTION, START, 0);
	FM_SET_FIELD(val, HLP_MAP_DGLORT_ACTION, ROT, 0);
	FM_SET_BIT(val, HLP_MAP_DGLORT_ACTION, ENABLE, 1);
	write64(HLP_MAP_DGLORT_ACTION(1, 0), val);

	if (outFp) fprintf(outFp, "# GLORT_CAM/TABLE\n");
	/* Match glorts for port 0..24 in cam addresses 0 and flood glort 0xffff in
	  address PORT_GLORT_FLOOD */
	val = 0;
	FM_SET_FIELD(val, HLP_GLORT_CAM, KEY, PORT_GLORT_BASE & 0xFFE0);
	FM_SET_FIELD(val, HLP_GLORT_CAM, KEY_INVERT, ~PORT_GLORT_BASE & 0xFFE0);
	write64(HLP_GLORT_CAM(0, 0), val);
	val = 0;
	FM_SET_FIELD(val, HLP_GLORT_CAM, KEY, 0xFFFF);
	FM_SET_FIELD(val, HLP_GLORT_CAM, KEY_INVERT, ~0xFFFF);
	write64(HLP_GLORT_CAM(PORT_FLOOD_IDX, 0), val);

	/* Set dest table pointers */
	val = 0;
	FM_SET_FIELD(val, HLP_GLORT_RAM, STRICT, 3);
	FM_SET_FIELD(val, HLP_GLORT_RAM, DEST_INDEX, 0); /* using DEST_TABLE[0] */
	FM_SET_FIELD(val, HLP_GLORT_RAM, DEST_COUNT, 1);
	FM_SET_FIELD(val, HLP_GLORT_RAM, RANGE_SUB_INDEX_A, 0x50);
	FM_SET_BIT(val, HLP_GLORT_RAM, HASH_ROTATION, 0);
	write64(HLP_GLORT_RAM(0, 0), val);
	val = 0;
	FM_SET_FIELD(val, HLP_GLORT_RAM, STRICT, 3);
	FM_SET_FIELD(val, HLP_GLORT_RAM, DEST_INDEX, 24); /* using DEST_TABLE[24] */
	FM_SET_FIELD(val, HLP_GLORT_RAM, DEST_COUNT, 1);
	write64(HLP_GLORT_RAM(PORT_FLOOD_IDX, 0), val);

	/* set dmask for port/sglorts 0-23 (starting 0x100) and for flood glort */
	for (i = 0 ; i < 24; i++) {
		val = 0;
		FM_SET_FIELD(val, HLP_GLORT_DEST_TABLE, DEST_MASK, (1 << i));
		/* use first 24 entries port dest table */
		write64(HLP_GLORT_DEST_TABLE(i, 0), val);
	}
	write64(HLP_GLORT_DEST_TABLE(24, 0), 0x1FFFFF);

	/* Set flood UC, flood MC and flood BC glorts */
	write64(HLP_FLOOD_GLORT_TABLE(0, 0), 0x0000ffffffffffff);

	if (outFp) fprintf(outFp, "# CM WM \n");
	/* Program CM to drop on SHARED_WM, with some RX_PRIVATE.
	 * TODO: enable softdrop mode
	 *
	 * TOTAL_MEM = 24576 segments x 256B segments = 6MB
	 * rx private = 10KB per port = 40x 256B segments
	 * Overshooot =
	 * 	24K - 21 ports * 10KB - 256 - 128
	 * 	= 23352
	 * Shared space = 23352 - 21 ports * 40 private segments
	 * 	= 22512
	*/
	write64(HLP_CM_GLOBAL_WM(0), 23352);
	write64(HLP_CM_SHARED_WM(0,0),  22512);
	for (i = 0 ; i < 22; i++) {
		write64(HLP_CM_TX_TC_PRIVATE_WM(i,0,0), 0);
		write64(HLP_CM_RX_SMP_PRIVATE_WM(i,0,0), 40);
	}

	write64(HLP_CM_CPK_PORT_NUM(0), cpkLogPort);

	/* I doubt hardware supports reprogramming the number of ports without
	 * resetting the switch. */
	val = 0;
	FM_SET_FIELD(val, HLP_CM_GLOBAL_CFG, NUM_SWEEPER_PORTS, 24);
	FM_SET_BIT(val, HLP_CM_GLOBAL_CFG, SWEEPER_EN, 1);
	write64(HLP_CM_GLOBAL_CFG(0), val);

	/* Program PORT_CFG_2 for Domain 0 */
	write64(HLP_FWD_PORT_CFG_2(0,0), 0xffffff);

	if (outFp) fprintf(outFp, "# VID TABLE\n");
	/* No need to add all ports to all VLANs. Only VLAN 1 is used as
	   described in the SAS. Same for MST table. */
	for (i = defaultVid ; i <= defaultVid ; i++) {
		/* Add all ports to all vlans */
		write64(HLP_INGRESS_VID_TABLE(i, 0), portMask);
		write64(HLP_EGRESS_VID_TABLE(i, 0), portMask);

		/* set STP state to forwarding */
		write64(HLP_INGRESS_MST_TABLE(defaultVid, 0),
					0xffffffffffffffff);
		write64(HLP_EGRESS_MST_TABLE(defaultVid, 0), 0xffffffff);
	}

	if (outFp) fprintf(outFp, "# FWD_PORT_CFG_1\n");
	/* Program PORT_CFG_1 for ethernet ports to only fwd to CPK*/
	for (i = 0 ; i < 20 ; i++) {
		val = 0;
		FM_SET_FIELD(val, HLP_FWD_PORT_CFG_1, DESTINATION_MASK,
			       (1 << cpkLogPort));
		FM_SET_BIT(val, HLP_FWD_PORT_CFG_1, LEARNING_ENABLE, 1);
		FM_SET_BIT(val, HLP_FWD_PORT_CFG_1, FILTER_VLAN_INGRESS, 1);
		write64(HLP_FWD_PORT_CFG_1(i, 0), val);
	}

	/* CPK traffic can go to any active port.
	 * NOTE: this must be specially programmed with knowledge of
	 *       active ports
	*/
	int tmpMask = 0;
	for ( i = 0 ; i < 8 ; i++) {
		if (quad_mode[i] == 1)
			tmpMask |= 0x1 << map_phys_to_log(i*4);
		else if (quad_mode[i] == 0)
			tmpMask |= 0xf << map_phys_to_log(i*4);
	}

	/* For CPK port the final TC associated with the packet
	   should be stored in the CD field. So CPK port should bet set to 01
	   in MOD_CD_FN. All other ports should be set to 00 in NVM. The API
	   will set the CPM ports appropriately when IPsec is enabled */
	write64(HLP_MOD_CD_FN(0),  (1ULL << (cpkLogPort*2)));

	/* These logical ports aren't used */
	for (i = 21; i < 24; i++)
		write64(HLP_FWD_PORT_CFG_1(i, 0), 0);

	printf("PhysicalPort	LogicalPort\n");
	for (i = 0; i < 32; i++) {
		if (map_phys_to_log(i) < 0) continue;
		printf("     %2d      =>     %2d\n", i, map_phys_to_log(i));
	}

	if (outFp) fprintf(outFp, "#  MAP_PORT_DEFAULT, MAP_LEN_LIMIT\n");
	for (i = 0 ; i < 24 ; i++)  {
		/* Set default vlan */
		val = 0;
		FM_SET_FIELD(val, HLP_MAP_PORT_DEFAULT, TARGET, 82);
		FM_SET_FIELD(val, HLP_MAP_PORT_DEFAULT, VALUE, defaultVid);
		write64(HLP_MAP_PORT_DEFAULT(i, 0, 0), val);
		/* bug 31834 */
		val = 0;
		FM_SET_FIELD(val, HLP_MAP_LEN_LIMIT, INR_MPLS_LEN_LIMIT, 5);
		FM_SET_FIELD(val, HLP_MAP_LEN_LIMIT, OTR_MPLS_LEN_LIMIT, 5);
		FM_SET_FIELD(val, HLP_MAP_LEN_LIMIT, INR_L2_LEN_LIMIT, 2);
		FM_SET_FIELD(val, HLP_MAP_LEN_LIMIT, OTR_L2_LEN_LIMIT, 2);
		write64(HLP_MAP_LEN_LIMIT(i, 0), val);
	}

	if (outFp) fprintf(outFp, "# MAP_PORT_CFG\n");
	/* Set the default SGLORT */
	for (i = 0 ; i < 24 ; i++) {
		val = 0;
		FM_SET_FIELD(val, HLP_MAP_PORT_CFG, DEFAULT_SGLORT,
			       (i+PORT_GLORT_BASE));
		FM_SET_BIT(val, HLP_MAP_PORT_CFG, DEFAULT_SGLORT_EN, 1);
		write64(HLP_MAP_PORT_CFG(i, 0), val);
	}

	if (outFp) fprintf(outFp, "# MAC_PIA_CFG\n");
	/* Program MAC_PIA_CFG for ethernet ports*/
	for (i = 0; i < 20 ; i++) {
		port = map_log_to_phys(i);
		val = 0;
		FM_SET_BIT(val, HLP_MAC_PIA_CFG, ONPI_ENABLE, 1);
		write64(HLP_MAC_PIA_CFG(port, 0), val);
	}

	port = map_log_to_phys(cpkLogPort);
	val = 0;
	FM_SET_BIT(val, HLP_MAC_PIA_CFG, ONPI_ENABLE, 1);
	FM_SET_BIT(val, HLP_MAC_PIA_CFG, XOFF_ENABLE, 1);
	FM_SET_BIT(val, HLP_MAC_PIA_CFG, ONPI_MASTER_MODE, 1);
	write64(HLP_MAC_PIA_CFG(port, 0), val);

	if (outFp) fprintf(outFp, "# Scheduler\n");
	/* Program the scheduler rings */
	for ( i = 0 ; i < 8 ; i++) {
		if (quad_mode[i] == 1) {
			printf("quad %d is 100G\n", i);
			write64(HLP_IARB_ADAPTIVE_SCHEDULE(0,i*4,0),
					(map_phys_to_log(i*4) << 16) | 0xffff);
			write64(HLP_IARB_ADAPTIVE_SCHEDULE(0,i*4+1,0),
					0);
			write64(HLP_IARB_ADAPTIVE_SCHEDULE(0,i*4+2,0),
					0);
			write64(HLP_IARB_ADAPTIVE_SCHEDULE(0,i*4+3,0),
					0);

			write64(HLP_EARB_ADAPTIVE_SCHEDULE(0,i*4,0),
					(map_phys_to_log(i*4) << 16) | 0xffff);
			write64(HLP_EARB_ADAPTIVE_SCHEDULE(0,i*4+1,0),
					0);
			write64(HLP_EARB_ADAPTIVE_SCHEDULE(0,i*4+2,0),
					0);
			write64(HLP_EARB_ADAPTIVE_SCHEDULE(0,i*4+3,0),
					0);
		}else if (quad_mode[i] == 0) {
			printf("quad %d is 25G\n", i);
			write64(HLP_IARB_ADAPTIVE_SCHEDULE(0,i*4,0),
					(map_phys_to_log(i*4) << 16) | 0x8888);
			write64(HLP_IARB_ADAPTIVE_SCHEDULE(0,i*4+1,0),
					(map_phys_to_log(i*4+1) << 16) | 0x4444);
			write64(HLP_IARB_ADAPTIVE_SCHEDULE(0,i*4+2,0),
					(map_phys_to_log(i*4+2) << 16) | 0x2222);
			write64(HLP_IARB_ADAPTIVE_SCHEDULE(0,i*4+3,0),
					(map_phys_to_log(i*4+3) << 16) | 0x1111);

			write64(HLP_EARB_ADAPTIVE_SCHEDULE(0,i*4,0),
					(map_phys_to_log(i*4) << 16) | 0x8888);
			write64(HLP_EARB_ADAPTIVE_SCHEDULE(0,i*4+1,0),
					(map_phys_to_log(i*4+1) << 16) | 0x4444);
			write64(HLP_EARB_ADAPTIVE_SCHEDULE(0,i*4+2,0),
					(map_phys_to_log(i*4+2) << 16) | 0x2222);
			write64(HLP_EARB_ADAPTIVE_SCHEDULE(0,i*4+3,0),
					(map_phys_to_log(i*4+3) << 16) | 0x1111);
		} else
			printf("quad %d is disabled\n", i);
	}

	/* Enable scheduler rings */
	val = 0;
	FM_SET_BIT(val, HLP_EARB_CTRL, ENABLE, 1);
	FM_SET_BIT(val, HLP_EARB_CTRL, ADAPTIVE_SCHED_ENABLE, 1);
	FM_SET_FIELD(val, HLP_EARB_CTRL, MAX_ROUND_INDEX, 0xF);
	write64(HLP_EARB_CTRL(0), val);
	val = 0;
	FM_SET_BIT(val, HLP_IARB_CTRL, ENABLE, 1);
	FM_SET_BIT(val, HLP_IARB_CTRL, ADAPTIVE_SCHED_ENABLE, 1);
	FM_SET_FIELD(val, HLP_IARB_CTRL, MAX_ROUND_INDEX, 0xF);
	FM_SET_FIELD(val, HLP_IARB_CTRL, ADAPTIVE_MIN_SPACING, 1);
	write64(HLP_IARB_CTRL(0), val);

	/* CPK port */
	val = 0;
	/* Test code assumes flooding to all ports */
	tmpMask = 0xFFFFF;
	FM_SET_FIELD(val, HLP_FWD_PORT_CFG_1, DESTINATION_MASK,
		       tmpMask);
	FM_SET_BIT(val, HLP_FWD_PORT_CFG_1, LEARNING_ENABLE, 1);
	FM_SET_BIT(val, HLP_FWD_PORT_CFG_1, FILTER_VLAN_INGRESS, 1);
	write64(HLP_FWD_PORT_CFG_1(cpkLogPort, 0), val);
}

static void printPacket(unsigned char *packet, int len)
{
	int i;
	int j;

	for (i = 0 ; i < len ; ) {
		printf("[%04d]: ", i);
		for( j = 0 ; j < 16 && i < len; j++, i++)
			printf("%02x ", packet[i]);
		printf("\n");
	}
}

static unsigned char *generateVlanPacket(unsigned char *dmac,
					 unsigned char *smac, int len, int vid)
{
	int i;
	unsigned char *packet;
	unsigned int crc;

	packet = malloc(len*sizeof(*packet));

	if(!packet) {
		printf("ERROR: allocating memory\n");
		exit(1);
	}

	memcpy(&packet[0], dmac, 6);
	memcpy(&packet[6], smac, 6);

	packet[13] = 0x81;
	packet[14] = 0x00;
	packet[15] = (vid >> 8) & 0xf;
	packet[16] = (vid & 0xff);

	for (i = 17 ; i < len-4 ; i++)
		packet[i] = i;

	crc = fmCrc32(packet, len-4);

	for (i = 0 ; i < 4; i++)
		packet[len-4+i] = (crc >> (8*i)) & 0xff;

	printPacket(packet, len);
	return packet;
}

static unsigned char *generatePacket( unsigned char *dmac, unsigned char *smac,
				int len)
{
	int i;
	unsigned char *packet;
	unsigned int crc;

	packet = malloc(len*sizeof(*packet));

	if(!packet) {
		printf("ERROR: allocating memory\n");
		exit(1);
	}

	memcpy(&packet[0], dmac, 6);
	memcpy(&packet[6], smac, 6);

	for (i = 12 ; i < len-4 ; i++)
		packet[i] = i;

	crc = fmCrc32(packet, len-4);

	for (i = 0 ; i < 4; i++)
		packet[len-4+i] = (crc >> (8*i)) & 0xff;

	printPacket(packet, len);
	return packet;
}

int main(int argc, char *argv[])
{
	int i;
	char *pktServerFile = NULL;
	unsigned char mac1[] = {0x00,0x01,0x02,0x03,0x04,0x05};
	unsigned char mac2[] = {0x00,0x07,0x08,0x09,0x0A,0x0B};
	unsigned char cpkmac[] = {0x00,0x06,0x05,0x04,0x03,0x02};
	int pktLen = 64;
	int logport;
	int exitOnErr = 1;

	if (argc < 1) {
		printf("Please specify models.packetServer file\n");
		exit(1);
	}

	if (argc >= 3) {
		printf("Saving register writes to file: %s\n", argv[2]);
		outFp = fopen(argv[2], "w+");
	}

	if (argc >= 4) {
		exitOnErr = !exitOnErr;
	}

	pktServerFile = argv[1];
	connectCpuToWm(pktServerFile);
	for ( i = 0 ; i < 32; i++)
		connectToWmEthernetPort(i);

	int quad_mode[8];
	unsigned int physPort = 17;
	unsigned int physPort2 = 25;
	unsigned int cpkPhysPort = 28;
	unsigned int cpkPort = map_phys_to_log(cpkPhysPort);


	for (i = 0 ; i < 8 ; i++)
		quad_mode[i] = 0;	/* 25G mode */
	quad_mode[7] = 1;		/* CPK 100G mode */
	quad_mode[1] = -1;		/* CPMs disabled */
	quad_mode[3] = -1;		/* CPMs disabled */

	if (outFp) {
		loadCfg(cpkPort, quad_mode);
		fclose(outFp);
	}
	outFp = NULL;

	/* Test cases:
	 * 1) Traffic from ports floods to CPK only, macs are learned
	 * 2) Traffic from CPK floods to ports, mac learned
	 * 3) Traffic from CPK to specific macs egresses correct ports
	 * 4) Traffic from ports to CPK mac forwards to CPK
	*/

	/*************************************************************
	 * Send packet in on ethernet port with unknown dmac
	 *************************************************************/
	printf("######## Sending %d-byte packet with unknown dmac on log port %d physport %d\n",
	       pktLen, map_phys_to_log(physPort), physPort);
	unsigned char *packet = generatePacket(mac1, mac2, pktLen);
	rimmonSendPkt(0, physPort, packet, pktLen);
	free(packet);

	unsigned char *packetRcv = NULL;
	unsigned char sbData[32];
	unsigned int lenRcv = -1;
	/* Packet should flood to only CPK port */
	for ( i = 0 ; i < 32 ; i++) {
		logport = map_phys_to_log(i);
		if (logport < 0) continue;
		printf("Checking log port %d phys port %d...\n", logport, i);
		recvPacket(i, 0.5*1000, &packetRcv, &lenRcv, sbData);

		if (logport == cpkPort && packetRcv != NULL)
			printf("Received packet on CPK logport %d len %d\n",
			       logport, lenRcv);
		else if (logport == cpkPort && packetRcv == NULL) {
			printf("ERROR: expecting packet on log port %d\n",logport);
			if (exitOnErr) exit(1);
		}
		else if (i != cpkPort && packetRcv != NULL) {
			printf("ERROR: not expecting packet on log port %d\n",logport);
			if (exitOnErr) exit(1);
		}

		if(packetRcv != NULL) free(packetRcv);
	}

	/*************************************************************
	 * Send packet in from cpk with unknown dmac
	 *************************************************************/
	printf("\n\n######## Sending %d-byte packet with unknown dmac on CPK physport %d logport %d\n",
	       pktLen, cpkPhysPort, cpkPort);
	packet = generatePacket(mac1, cpkmac, pktLen);
	cpkSendPkt(0, cpkPhysPort, packet, pktLen);
	free(packet);

	/* Packet should flood to all ports */
	for ( i = 0 ; i < 32 ; i++) {
		logport = map_phys_to_log(i);
		if (logport < 0) continue;
		printf("Checking physport %d logport %d...\n", i, logport);
		recvPacket(i, 0.5*1000, &packetRcv, &lenRcv, sbData);

		if (logport < cpkPort) {
			if (packetRcv == NULL) {
				printf("ERROR: expected frame on logport %d\n",logport);
				if (exitOnErr) exit(1);
			}else
				printf("saw frame on logport %d len %d as expected\n",
						logport, lenRcv);
		} else if (logport == cpkPort) {
			if (packetRcv != NULL) {
				printf("ERROR: saw frame on cpk port %d len %d\n",
						logport, lenRcv);
				if (exitOnErr) exit(1);
			}
		} else {
			if (packetRcv != NULL) {
				printf("ERROR: saw frame on logport %d len %d\n",
						logport, lenRcv);
				if (exitOnErr) exit(1);
			}
		}

		if(packetRcv != NULL) free(packetRcv);
	}

	/*************************************************************
	 * Send packet in from cpk with known dmac
	 *************************************************************/
	printf("\n\n######## Sending %d-byte packet with known dmac on CPK logport %d\n",
	       pktLen, cpkPort);
	packet = generatePacket(mac2, cpkmac, pktLen);
	cpkSendPkt(0, cpkPhysPort, packet, pktLen);
	free(packet);

	/* Packet should only come out 1 ethernet port */
	for ( i = 0 ; i < 32 ; i++) {
		logport = map_phys_to_log(i);
		if (logport < 0) continue;
		printf("Checking physport %d logport %d...\n", i, logport);
		recvPacket(i, 0.5*1000, &packetRcv, &lenRcv, sbData);

		if (i == physPort) {
			if (packetRcv == NULL) {
				printf("ERROR: exected frame on logport %d\n",logport);
				if (exitOnErr) exit(1);
			} else
				printf("saw frame on logport %d len %d as expected\n", logport, lenRcv);
		} else {
			if (packetRcv != NULL) {
				printf("ERROR: saw frame on logport %d len %d\n",
						logport, lenRcv);
				if (exitOnErr) exit(1);
			}
		}

		if(packetRcv != NULL) free(packetRcv);
	}

	/*************************************************************
	 * Send packet in from ethernet to CPK
	 *************************************************************/
	printf("\n\n######## Sending %d-byte packet on logport %d\n", pktLen, 
			map_phys_to_log(physPort2));
	packet = generatePacket(cpkmac, mac1, pktLen);
	rimmonSendPkt(0, physPort2, packet, pktLen);
	free(packet);

	/* Packet should only come out cpk port */
	for ( i = 0 ; i < 32 ; i++) {
		logport = map_phys_to_log(i);
		if (logport < 0) continue;
		printf("Checking physport %d logport %d...\n", i, logport);
		recvPacket(i, 0.5*1000, &packetRcv, &lenRcv, sbData);

		if (logport == cpkPort) {
			if (packetRcv == NULL)  {
				printf("ERROR: exected frame on cpk logport %d\n",logport);
				if (exitOnErr) exit(1);
			}else {
				printf("saw frame on cpk port %d len %d as expected\n",
						logport, lenRcv);
				printPacket(packetRcv, lenRcv);
			}
		} else {
			if (packetRcv != NULL) {
				printf("ERROR: saw frame on logport %d len %d\n",
						logport, lenRcv);
				if (exitOnErr) exit(1);
			}
		}

		if(packetRcv != NULL) free(packetRcv);
	}

	/*************************************************************
	 * Send packet in vlan tagged packet to cpk
	 *************************************************************/
	int vlanId = 4091;
	printf("\n\n######## Sending %d-byte vlanId %d packet on logport %d\n",
	       pktLen, vlanId, map_phys_to_log(physPort2));
	packet = generateVlanPacket(cpkmac, mac1, pktLen, vlanId);
	rimmonSendPkt(0, physPort2, packet, pktLen);
	free(packet);

	/* Packet should only come out cpk port */
	for ( i = 0 ; i < 32 ; i++) {
		logport = map_phys_to_log(i);
		if (logport < 0) continue;
		printf("Checking physport %d logport %d...\n", i, logport);
		recvPacket(i, 0.5*1000, &packetRcv, &lenRcv, sbData);

		if (logport == cpkPort) {
			if (packetRcv == NULL) {
				printf("ERROR: exected frame on cpk port %d\n",
				       logport);
				if (exitOnErr) exit(1);
			}else {
				int gotId = (packetRcv[15] << 8)| packetRcv[16];
				if (gotId != vlanId) {
					printf("ERROR: expect vlanId %d but got %d\n",
					       vlanId, gotId);
				}
				printf("saw frame on cpk port %d len %d vlanId %d as expected\n",
						logport, lenRcv, gotId);
				printPacket(packetRcv, lenRcv);
			}
		} else {
			if (packetRcv != NULL) {
				printf("ERROR: saw frame on log port %d len %d\n",
						logport, lenRcv);
				if (exitOnErr) exit(1);
			}
		}

		if(packetRcv != NULL) free(packetRcv);
	}
	cleanup();
	exit(0);
}
